package BOJ;

import java.util.Scanner;

public class boj2742 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		int n = sc.nextInt();
		sc.nextLine();

		for (int i = 1; i <= 1; i++) {
			for (int j = n; j >= 1; j--) {
				System.out.println(j);
			}
		}
		System.out.println();
	}
}